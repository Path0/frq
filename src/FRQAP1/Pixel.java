package FRQAP1;

public class Pixel {
	private int red;
	private int green;
	private int blue;
	
	
	public Pixel(int r, int g, int b) {
		red = r;
		green = g;
		blue = b;
	}
	
	public int getRed() {
		return red;
	}
	public int getGreen() {
		return green;
	}
	
	public int getBlue() {
		return blue;
	}

}
